<hr>									<a name="a6"></a>
		<h2>Jenis Ujian</h2>

<hr>
<ol>
	<li><strong>Ujian Tertulis (PBT atau CBT)</strong></li>
</ol>

<p style="margin-left:31.5pt;">Materi Ujian Tertulis terdiri atas:</p>

<ol style="list-style-type:lower-alpha;">
	<li>Tes Kemampuan dan Potensi Akademik (TKPA).</li>
	<li>Tes Kemampuan Dasar Sains dan Teknologi (TKD Saintek) terdiri atas mata uji Matematika, Biologi, Kimia, dan Fisika.</li>
	<li>Tes Kemampuan Dasar Sosial dan Humaniora (TKD Soshum) terdiri atas mata uji Sosiologi, Sejarah, Geografi, dan Ekonomi.</li>
</ol>

<ol>
	<li value="2"><strong>Ujian Keterampilan </strong></li>
</ol>

<p style="margin-left:.5in;">Ujian Keterampilan diperuntukkan bagi peserta yang memilih program studi bidang seni dan keolahragaan terdiri dari :</p>

<ol style="list-style-type:lower-alpha;">
	<li>Ujian Keterampilan seni rupa dengan mata uji tes menggambar, tes pengetahuan dan wawasan seni rupa.</li>
	<li>Ujian Keterampilan seni tari dengan mata uji tes tari bentuk, tes kreatifitas tari, tes imitasi gerak, serta tes pengetahuan dan wawasan seni tari (dalam bentuk wawancara).</li>
	<li>Ujian Keterampilan seni musik dengan mata uji tes musikalitas, tes praktik instrumen, serta tes pengetahuan dan wawasan seni musik (dalam bentuk wawancara).</li>
	<li>Ujian Keterampilan seni drama/teater dengan mata uji tes praktik monolog, tes pantomim, serta tes pengetahuan dan wawasan seni drama/teater (dalam bentuk wawancara)</li>
	<li>Ujian Keterampilan olahraga dengan mata uji tes kesehatan dan tes keterampilan motorik.</li>
</ol>

<p style="margin-left:35.45pt;">Peserta dapat mengikuti Ujian Keterampilan di PTN terdekat yang memiliki program studi yang sesuai dengan pilihan peserta. Daftar PTN penyelenggara ujian keterampilan secara lengkap dapat dilihat di laman <a href="http://www.sbmptn.ac.id/">http://www.sbmptn.ac.id</a>.</p>

<hr>									<a name="a7"></a>
		<h2>Kelompok Ujian</h2>

<hr>
<p style="margin-left:14.2pt;">Kelompok ujian SBMPTN terbagi menjadi 3 (tiga) jenis, yaitu:</p>

<ol>
	<li>Kelompok Ujian Sains dan Teknologi (Saintek) dengan materi ujian TKPA dan TKD Saintek.</li>
	<li>Kelompok Ujian Sosial dan Humaniora (Soshum) dengan materi ujian TKPA dan TKD Soshum.</li>
	<li>Kelompok Ujian Campuran dengan materi ujian TKPA, TKD Saintek, dan TKD Soshum.</li>
</ol>

<p style="margin-left:14.2pt;">Setiap peserta dapat mengikuti kelompok ujian Saintek, Soshum, atau Campuran.</p>

<hr>									<a name="a8"></a>
		<h2>Kelompok Program Studi dan Jumlah Pilihan</h2>

<hr>
<ol>
	<li>Program Studi yang ada di PTN dibagi menjadi dua kelompok, yaitu kelompok Saintek&nbsp; dan kelompok Soshum.</li>
	<li>Peserta dapat memilih program studi sebanyak-banyaknya 3 (tiga) program studi dengan ketentuan sebagai berikut:
	<ol style="list-style-type:lower-alpha;">
		<li>Jika program studi yang dipilih semuanya dari kelompok Saintek, maka peserta mengikuti kelompok ujian Saintek.</li>
		<li>Jika program studi yang dipilih semuanya dari kelompok Soshum, maka peserta mengikuti kelompok ujian Soshum.</li>
		<li>Jika program studi yang dipilih terdiri dari kelompok Saintek dan Soshum, maka peserta mengikuti kelompok ujian Campuran.</li>
	</ol>
	</li>
	<li>Urutan dalam pemilihan program studi menyatakan prioritas pilihan.</li>
	<li>Peserta ujian yang hanya memilih 1 (satu) program studi dapat memilih program studi di PTN manapun.</li>
	<li>Peserta ujian yang memilih 2 (dua) atau 3 (tiga) program studi, salah satu program studi pilihannya harus di PTN yang berada dalam satu wilayah pendaftaran dengan tempat peserta mengikuti ujian. Pilihan Program Studi yang lain dapat di PTN yang berada di luar wilayah pendaftaran tempat peserta mengikuti ujian.</li>
	<li>Daftar wilayah pendaftaran, program studi, daya tampung per PTN tahun 2017, dan jumlah peminat program studi per PTN tahun 2017 dapat dilihat di laman <a href="http://www.sbmptn.ac.id/">http://www.sbmptn.ac.id</a> mulai tanggal 4 April 2017.</li>
</ol>

