	<a name="a1"></a>
		<h2>Kata Pengantar</h2>

<hr>
<p>Berdasarkan Undang-Undang Nomor 12 Tahun 2012 tentang Pendidikan Tinggi, Peraturan Pemerintah Nomor 4 Tahun 2014 tentang Penyelenggaraan Pendidikan Tinggi dan Pengelolaan Perguruan Tinggi, Peraturan Menteri Riset, Teknologi, dan Pendidikan Tinggi Nomor 126 Tahun 2016 &nbsp;tentang Penerimaan Mahasiswa Baru Program Sarjana pada Perguruan Tinggi Negeri, dilakukan melalui: Seleksi Nasional Masuk Perguruan Tinggi Negeri (SNMPTN), Seleksi Bersama Masuk Perguruan Tinggi Negeri (SBMPTN), dan Seleksi Mandiri. SBMPTN&nbsp; 2017 adalah seleksi berdasarkan hasil ujian tertulis dalam bentuk cetak (<em>paper based testing</em>) atau menggunakan komputer (<em>computer based testing</em>), atau kombinasi hasil ujian tertulis dan ujian keterampilan calon Mahasiswa, dilakukan secara bersama di bawah koordinasi panitia pusat.</p>

<p>Pembiayaan penyelenggaraan SBMPTN dibebankan kepada peserta seleksi dan Kementerian Riset, Teknologi dan Pendidikan Tinggi. Peserta SBMPTN dari keluarga kurang mampu secara ekonomi dan mempunyai prestasi akademik tinggi dapat mengajukan dana bantuan biaya pendidikan Bidikmisi.&nbsp;</p>

<p>Informasi SBMPTN 2017 meliputi: ketentuan dan persyaratan umum, tata cara pembayaran biaya seleksi, tata cara pendaftaran, jadwal pelaksanaan, dan jumlah pilihan PTN serta program studi. Secara rinci, informasi lengkap mengenai SBMPTN 2017 dapat dilihat di laman <a href="http://www.sbmptn.ac.id/">http://www.sbmptn.ac.id</a>.</p>

<p>&nbsp;</p>

<hr>									<a name="a2"></a>
		<h2>Latar Belakang</h2>

<hr>
<p>Seleksi bersama dalam penerimaan mahasiswa baru di lingkungan PTN&nbsp; melalui ujian tertulis secara nasional yang selama ini telah dilakukan menunjukkan berbagai keuntungan dan keunggulan, baik bagi peserta, PTN, maupun bagi kepentingan nasional. Bagi peserta, seleksi bersama menguntungkan karena lebih efisien, murah, dan fleksibel. Hal ini dikarenakan adanya mekanisme lintas wilayah.</p>

<p>Berdasarkan pengalaman dalam melaksanakan SBMPTN, yaitu seleksi penerimaan mahasiswa baru melalui ujian tertulis atau kombinasi hasil ujian tertulis dan ujian keterampilan, maka pada tahun 2017 melaksanakan Ujian tertulis yang terdiri dari <em>Paper-Based Testing</em> (PBT) dan <em>Computer-Based Testing</em> (CBT), yang dapat diikuti oleh siswa lulusan tahun 2015, 2016, dan 2017 dari pendidikan menengah (SMA/MA/SMK) dan sederajat, serta lulusan Paket C tahun 2015, 2016, dan 2017.</p>

<p>Ujian tertulis yang terdiri dari PBT dan CBT menggunakan soal-soal ujian yang dirancang mengikuti kaidah-kaidah akademik pengembangan tes. Ujian tertulis SBMPTN dirancang untuk mengukur kemampuan dasar yang dapat memprediksi keberhasilan calon mahasiswa di semua program studi, yakni kemampuan penalaran tingkat tinggi (<em>higher order thinking</em>), yang meliputi potensi akademik, penguasaan bidang studi dasar, bidang sains dan teknologi (saintek) dan/atau bidang sosial dan humaniora (soshum). Selain mengikuti ujian tertulis (PBT atau CBT), peserta yang memilih program studi Ilmu Seni dan/atau Keolahragaan diwajibkan mengikuti ujian keterampilan.</p>

<p>&nbsp;</p>

<hr>									<a name="a3"></a>
		<h2>Tujuan</h2>

<hr>
<p>Tujuan SBMPTN 2017 adalah sebagai berikut:</p>

<ol>
	<li>Menyeleksi calon mahasiswa yang diprediksi mampu menyelesaikan studi di perguruan tinggi dengan baik.</li>
	<li>Memberi kesempatan bagi calon mahasiswa untuk memilih lebih dari satu PTN lintas wilayah.</li>
</ol>

<p>&nbsp;</p>

<hr>									<a name="a4"></a>
		<h2>Ketentuan Umum dan Persyaratan</h2>

<hr>
<p><strong>1.&nbsp;&nbsp; Ketentuan Umum</strong></p>

<p>SBMPTN 2017 merupakan seleksi yang dilakukan oleh&nbsp; PTN di lingkungan Kementerian Riset, Teknologi, dan Pendidikan Tinggi (Kemristekdikti) dan Kementerian Agama secara bersama di bawah koordinasi Panitia Pusat dengan seleksi berdasarkan hasil ujian tertulis dalam bentuk cetak (<em>Paper Based Testing</em>) atau menggunakan komputer (<em>Computer Based Testing</em>), atau kombinasi hasil ujian tertulis dan ujian keterampilan calon mahasiswa.</p>

<p>&nbsp;</p>

<p><strong>2.&nbsp;&nbsp; Persyaratan</strong></p>

<ol style="list-style-type:lower-alpha;">
	<li><strong>Pendaftaran</strong>

	<ol>
		<li>Bagi lulusan SMA/SMK/MA atau sederajat dan Paket C tahun 2015 dan 2016&nbsp; harus memiliki ijazah.</li>
		<li>Bagi lulusan SMA/SMK/MA atau sederajat dan Paket C tahun 2017 telah memiliki Surat Keterangan Lulus Pendidikan Menengah atau Ijasah Paket C. Surat Keterangan Lulus sekurang-kurangnya memuat informasi jati diri dan foto terbaru yang bersangkutan serta dibubuhi cap yang sah.</li>
		<li>Peserta seleksi memiliki kesehatan yang memadai sehingga tidak mengganggu kelancaran proses pembelajaran di program studinya.</li>
	</ol>
	</li>
	<li><strong>Penerimaan</strong>
	<p style="margin-left:28.35pt;">Peserta seleksi lulus pendidikan menengah, lulus SBMPTN 2017, sehat, dan memenuhi persyaratan lain yang ditentukan oleh masing-masing PTN penerima.</p>
	</li>
</ol>

